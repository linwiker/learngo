// 使用 * 符号打印宽为20，高为10的矩形。
package main

import "fmt"

func main()  {
	w, h := 20, 10
	for y := 0; y < h; y++ {
		for x := 0; x < w; x++ {
			fmt.Print("*")
		}
		fmt.Println()
	}
}

/* Output:
********************
********************
********************
********************
********************
********************
********************
********************
********************
********************
*/